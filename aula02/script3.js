var valor1 = 9;
var valor2 = 3;


// Adição
function adicao() {
    var soma = valor1 + valor2;
    console.log(valor1 + ' + ' + valor2 + ' = ' + soma);
}

adicao();

// Subtração
function subtracao() {
    var soma = valor1 - valor2;
    console.log(valor1 + ' - ' + valor2 + ' = ' + soma);
}

subtracao();

// Divisão
function divisao() {
    var soma = valor1 / valor2;
    console.log(valor1 + ' : ' + valor2 + ' = ' + soma);
}

divisao();

// Multiplicação
function multiplicacao() {
    var soma = valor1 * valor2;
    console.log(valor1 + ' * ' + valor2 + ' = ' + soma);
}

multiplicacao();
